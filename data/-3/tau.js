import {Vector2} from "three";

//the hexagonal torus
const tau = new Vector2(-1/2, Math.sqrt(3)/2);

//these come directly from tau
let curveArea = 4*Math.PI*tau.x;
let curveLength = 4*Math.PI*tau.y;


//CURVE FOR THE HEX TORUS
let coordCurve = function(t){

    // let a =0.104;
    // let b = 2.67;
    // let n = 6;

    let a =0.276;
    let b =1.9;
    let n = 3;

    // let a =0.42;
    // let b = 2.6;
    // let n = 2;

    // let a =0.028;
    // let b = 3.46;
    // let n = 18;

    return {
        phi: Math.PI/2 + a*b*Math.cos(n*t),
        theta: t + a*Math.sin(2*n*t)
    };
}




//takes in points in the standard FD gen by 1 and tau
//changes to the lattice used by the hopf parameterization
//generated by 2pi and (a/2,l/2)
let fromTauCoords = function(data) {
    //data is of form [x,y];
    let x = data[0];
    let y = data[1];
    //for this lattice with Re(tau)=1/2, it is simply a rescaling!
    return new Vector2(x,y).multiplyScalar(2*Math.PI);
}


let latticeData = {
    area: curveArea,
    length:curveLength,
    tau:tau,
    fromTauCoords:fromTauCoords
}



export {coordCurve,latticeData};
